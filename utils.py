def calculate_month(filename):
    weeks = int(filename[:3])
    months = weeks // 4
    return months

# Function to map a month to its interval group
def get_month_group(month, month_intervals):
    for group, interval in month_intervals.items():
        if month in interval:
            return group
    return None

